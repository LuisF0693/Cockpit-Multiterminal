#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const yaml = require('js-yaml');

const ROOT = process.cwd();

function parseArgs(argv) {
  const args = {
    business: 'aiox',
    outDir: null,
  };

  for (const raw of argv) {
    if (raw.startsWith('--business=')) {
      args.business = raw.slice('--business='.length).trim() || 'aiox';
      continue;
    }
    if (raw.startsWith('--out-dir=')) {
      args.outDir = raw.slice('--out-dir='.length).trim() || null;
    }
  }

  return args;
}

function ensureDir(target) {
  fs.mkdirSync(target, { recursive: true });
}

function fail(message) {
  process.stderr.write(`ERROR: ${message}\n`);
  process.exit(1);
}

function readYamlFile(filePath) {
  if (!fs.existsSync(filePath)) {
    fail(`Missing YAML file: ${path.relative(ROOT, filePath)}`);
  }
  return yaml.load(fs.readFileSync(filePath, 'utf8')) || {};
}

function normalizeString(value) {
  return typeof value === 'string' ? value.trim() : '';
}

function collectVarsFromCssInventory(tokensDoc) {
  const candidates = [
    tokensDoc?.mapping_inventory?.css_vars,
    tokensDoc?.extraction?.token_inventory?.full_inventory,
    tokensDoc?.extraction?.token_inventory?.semantic_tokens,
  ];
  const merged = candidates
    .filter((item) => Array.isArray(item))
    .flat()
    .map((item) => normalizeString(item))
    .filter((item) => item.startsWith('--'));
  return [...new Set(merged)];
}

function buildCssVariables(vars) {
  const header = [
    '/* Generated by squads/design-ops/scripts/build-token-pipeline.cjs */',
    '/* Workspace-first contract: values are resolved downstream by consumer pipelines. */',
    ':root {',
  ];
  const body = vars.map((token) => `  ${token}: initial;`);
  return `${[...header, ...body, '}'].join('\n')}\n`;
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const designRoot = path.join(
    ROOT,
    'workspace',
    'businesses',
    args.business,
    'L2-tactical',
    'design'
  );

  const tokensPath = path.join(designRoot, 'tokens.yaml');
  const foundationsPath = path.join(designRoot, 'foundations.yaml');
  const componentsPath = path.join(designRoot, 'component-contracts.yaml');
  const motionPath = path.join(designRoot, 'motion-primitives.yaml');

  const tokensDoc = readYamlFile(tokensPath);
  const foundationsDoc = readYamlFile(foundationsPath);
  const componentsDoc = readYamlFile(componentsPath);
  const motionDoc = readYamlFile(motionPath);

  const registryVars = collectVarsFromCssInventory(tokensDoc);
  if (registryVars.length === 0) {
    fail('Token inventory is empty. Expected mapping_inventory.css_vars in tokens.yaml');
  }

  const outputRoot = args.outDir
    ? path.resolve(ROOT, args.outDir)
    : path.join(ROOT, 'outputs', 'design-ops', 'token-pipeline', args.business);
  ensureDir(outputRoot);

  const jsonOutputPath = path.join(outputRoot, 'tokens.json');
  const cssOutputPath = path.join(outputRoot, 'tokens.css');
  const metaOutputPath = path.join(outputRoot, 'manifest.json');

  fs.writeFileSync(
    jsonOutputPath,
    `${JSON.stringify(
      {
        business: args.business,
        generated_at: new Date().toISOString(),
        source_of_truth: `workspace/businesses/${args.business}/L2-tactical/design`,
        tokens: registryVars,
      },
      null,
      2
    )}\n`,
    'utf8'
  );
  fs.writeFileSync(cssOutputPath, buildCssVariables(registryVars), 'utf8');
  fs.writeFileSync(
    metaOutputPath,
    `${JSON.stringify(
      {
        business: args.business,
        generated_at: new Date().toISOString(),
        input_contracts: [
          path.relative(ROOT, tokensPath),
          path.relative(ROOT, foundationsPath),
          path.relative(ROOT, componentsPath),
          path.relative(ROOT, motionPath),
        ],
        outputs: [
          path.relative(ROOT, jsonOutputPath),
          path.relative(ROOT, cssOutputPath),
        ],
        publication: {
          mode: 'artifact_only',
          versioning: 'date-based',
        },
      },
      null,
      2
    )}\n`,
    'utf8'
  );

  process.stdout.write('PASS: token pipeline artifacts built\n');
  process.stdout.write(`  json=${path.relative(ROOT, jsonOutputPath)}\n`);
  process.stdout.write(`  css=${path.relative(ROOT, cssOutputPath)}\n`);
  process.stdout.write(`  manifest=${path.relative(ROOT, metaOutputPath)}\n`);
}

main();
